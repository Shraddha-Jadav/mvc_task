        public override string[] GetRolesForUser(string username)
        {
            using (shraddha_crmEntities2 _Context = new shraddha_crmEntities2())
           {
                var userRoles = (from employee in _Context.Employees
                                 join role in _Context.Departments
                                 on employee.DepartmentId equals role.DepartmentId
                                 where employee.Email == username
                                 select role.Name).ToArray();
                return userRoles;
            }
